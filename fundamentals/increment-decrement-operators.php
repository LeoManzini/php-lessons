	<?php 
		$x = 10;
		echo $x;
		echo "<br>";

		// Post incrementing
		$x++;
		echo $x;
		echo "<br>";

		// Pre incrementing
		++$x;
		echo $x;
		echo "<br>";

		// Post decrement
		$x--;
		echo $x;
		echo "<br>";

		// Pre decrement
		--$x;
		echo $x;
		echo "<br>";
	?>
